package moduleSecond.lesson27102020.ReturnValue;

public class ReturnValue {
    private String value;
    private String status;

    public ReturnValue(String value, String status) {
        this.value = value;
        this.status = status;
    }

    public String getValue() {
        return value;
    }

    public String getStatus() {
        return status;
    }
}
